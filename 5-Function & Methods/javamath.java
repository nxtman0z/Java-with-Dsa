//java math method example
public class javamath {
    public static void main(String[] args) {
        //abs() returns the absolute number
        System.out.println("Absolute number is :" + Math.abs(-12));
        //min() returns the minimun between two numbers
        System.out.println("Minimum number is : " + Math.min(10,5));
        //max() returns the maximun bettwo numbers
        System.out.println("Maximum number is :" + Math.max(13,54));
        //sqrt()finds the square root of a number
        System.out.println("Square root is :" +Math.sqrt(45));
        //pow() finds the power of a number as ur choice
        System.out.println("power is :" + Math.pow(10,2));
        //random() finds a random number 
        System.out.println("Random number is  :" + (int)(Math.random()*12));
    }
}
